function mostrar() {
    document.getElementById('llenado-Formulario').style.display = 'block';
    document.getElementById('mostrarFormulario').style.display = 'none';
    document.getElementById('ocultarFormulario').style.display = 'block';



} 

function ocultar() {
    document.getElementById('llenado-Formulario').style.display = 'none';
    document.getElementById('mostrarFormulario').style.display = 'block';
    document.getElementById('ocultarFormulario').style.display = 'none';

}